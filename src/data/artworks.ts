export const getArtworkSlug = (artwork: Artwork): string => slugify(artwork.title);

export const getArtworkBySlug = (slug: string): Artwork | undefined =>
  allArtworks.find((a) => getArtworkSlug(a) === slug);

export const getRelatedArtworks = (artwork: Artwork, limit = 3): Artwork[] =>
  allArtworks
    .filter((a) => a.collection === artwork.collection && getArtworkSlug(a) !== getArtworkSlug(artwork))
    .slice(0, limit);
